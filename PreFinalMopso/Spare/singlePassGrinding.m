function z =singlePassGrinding(x,varMin,varMax)
	docu = x(1)/1000 ;
	fa     = x(2) ;
	vs     = x(3) ;	
	y =7.2323+0.0429*(vs-19)/5 ...
 	  +4.3068*(docu-0.003)/0.002 ...
 	  +0.3855*(fa-740)/260 ...
 	  -0.146*((vs-19)/5)*((docu-0.003)/0.002) ...
 	  -0.1765*((vs-19)/5)*((fa-740)/260) ...
 	  +0.1453*((docu-0.003)/0.002)*((fa-740)/260) ...
 	  +3.3457*((docu-0.003)/0.002)*((docu-0.003)/0.002) ...
 	  -0.0217*((fa-740)/260)*((fa-740)/260);
    % y=7.2323+0.0429*(vs-19)/5+4.3068*(docu-0.003)/0.002+0.3855*(fa-740)/260-0.146*((vs-19)/5)*((docu-0.003)/0.002)-0.1765*((vs-19)/5)*((fa-740)/260)+0.1453*((docu-0.003)/0.002)*((fa-740)/260)+3.3457*((docu-0.003)/0.002)*((docu-0.003)/0.002)-0.0217*((fa-740)/260)*((fa-740)/260);
	f2 = -docu * fa;

	nVar = numel(x);
	G = 0;
	for i = 1:nVar
  		G = G + sqrt((x(i) - max (varMin(i), x(i) ) )^2 +(x(i) - min(x(i) ,  varMax(i)))^2);
	end
	f1 = y + G;
	f2 = f2 + G;

	z = [f1
	 f2];
end 
